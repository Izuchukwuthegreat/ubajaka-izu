// Traditional function
function add(a, b) {
    return a + b;
}

// Arrow function
const add = (a, b) => a + b;

const areaOfRectangle = (length, width) => length * width;
console.log(areaOfRectangle(5, 10)); // Output: 50

const cube = (n) => n ** 3;
console.log(cube(5)); // Output: 125